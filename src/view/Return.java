    private boolean check(String ID, String equipID) throws SQLException, Exception {
        //throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
        if(StringUtil.isEmpty(ID)){
            if(type.equals("学生")){
                JOptionPane.showMessageDialog(null, "学号不能为空！");
                return false;
            }
            else{
                JOptionPane.showMessageDialog(null, "教工号不能为空！");
                return false;
            }
        }
        if(StringUtil.isEmpty(equipID)){
            JOptionPane.showMessageDialog(null, "器材编号不能为空！");
            return false;
        }
        if(type.equals("学生")){
            if(!StringUtil.isAllNumber(ID)){
                JOptionPane.showMessageDialog(null, "请输入合法的学号！");
                return false;
            }
            if(ID.length() != 14){
                JOptionPane.showMessageDialog(null, "请输入14位的学号！");
                return false;
            }
        }
        if(type.equals("老师")){
            if(!StringUtil.isAllNumber(ID)){
                JOptionPane.showMessageDialog(null, "请输入合法的教工号！");
                return false;
            }
            if(ID.length() != 11){
                JOptionPane.showMessageDialog(null, "请输入11位的教工号！");
                return false;
            }
        }
        if(!StringUtil.isAllNumber(equipID)){
            JOptionPane.showMessageDialog(null, "请输入合法的器材编号！");
            return false;
        }
        Connection con = dbUtil.getConnection();
        if(type.equals("老师")){
            if(!returnInfo.teacherReturnCheck(con, ID, equipID)){
                JOptionPane.showMessageDialog(null, "当前借用记录未找到，请输入正确的信息！");
                return false;
            }
            con = dbUtil.getConnection();
            if(!returnInfo.equipStateTeacherCheck(con, ID, equipID)){
                JOptionPane.showMessageDialog(null, "当前器材已归还，请勿重复操作！");
                return false;
            }
        }
        if(type.equals("学生")){
            if(!returnInfo.studentReturnCheck(con, ID, equipID)){
                JOptionPane.showMessageDialog(null, "当前借用记录未找到，请输入正确的信息！");
                return false;
            }
            con = dbUtil.getConnection();
            if(!returnInfo.equipStateStudentCheck(con, ID, equipID)){
                JOptionPane.showMessageDialog(null, "当前器材已归还，请勿重复操作！");
                return false;
            }
        }
        return true;
    }
