    private void returnButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_returnButtonActionPerformed

        // TODO add your handling code here:
        String ID = this.IDText.getText();
        String equipID = this.equipIDText.getText();
        try {
            if(check(ID,equipID)){//检查本次归还信息的合法性
                Connection con = dbUtil.getConnection();
                if(type.equals("学生")){
                    int returnResult = returnInfo.studentReturnOperation(con, ID, equipID);
                    if(returnResult == 1){
                        con = dbUtil.getConnection();
                        int inTimeResult = returnInfo.studentReturnCheckInTime(con, ID, equipID);
                        if(inTimeResult < 0){
                            con = dbUtil.getConnection();
                            blackInfo.updateBlackList(con, ID, "学生");
                            con = dbUtil.getConnection();
                            equipmentInfo.setReturn(con, equipID);
                            JOptionPane.showMessageDialog(null, "归还成功！本次归还已逾期"+String.valueOf(-inTimeResult)+"天！系统已自动将该用户列入黑名单！");
                        }
                        else{
                            con = dbUtil.getConnection();
                            equipmentInfo.setReturn(con, equipID);
                            JOptionPane.showMessageDialog(null, "归还成功！");
                        }
                        resetValues();
                    }
                    else{
                        JOptionPane.showMessageDialog(null, "归还失败！");
                        return;
                    }
                }
                if(type.equals("老师")){
                    int returnResult = returnInfo.teacherReturnOperation(con, ID, equipID);
                    if(returnResult == 1){
                        con = dbUtil.getConnection();
                        int inTimeResult = returnInfo.teacherReturnCheckInTime(con, ID, equipID);
                        if(inTimeResult < 0){
                            con = dbUtil.getConnection();
                            blackInfo.updateBlackList(con, ID, "老师");
                            con = dbUtil.getConnection();
                            equipmentInfo.setReturn(con, equipID);
                            JOptionPane.showMessageDialog(null, "归还成功！本次归还已逾期"+String.valueOf(-inTimeResult)+"天！系统已自动将该用户列入黑名单！");
                        }
                        else{
                            con = dbUtil.getConnection();
                            equipmentInfo.setReturn(con, equipID);
                            JOptionPane.showMessageDialog(null, "归还成功！");
                        }
                        resetValues();
                    }
                    else{
                        JOptionPane.showMessageDialog(null, "归还失败！");
                        return;
                    }
                }
            }
        }catch(Exception e){
            e.printStackTrace();
        }

    }//GEN-LAST:event_returnButtonActionPerformed

