    //初始化借用历史信息表格
    private void initBorrowHistoryTable(String type){
        DefaultTableModel dtm = (DefaultTableModel)borrowHistoryTable.getModel();
        dtm.setRowCount(0);
        Connection con = null;
        try{
            con = dbUtil.getConnection();
            ResultSet rs;
            if(type.equals("按学生")){
                rs = borrowInfo.listAllStudentBorrowHistory(con);
                while(rs.next()){
                    Vector vec = new Vector();
                    vec.add(rs.getInt("ID"));
                    vec.add(rs.getString("stuID"));
                    vec.add(rs.getString("stuName"));
                    vec.add(rs.getString("equipID"));
                    vec.add(rs.getString("equipName"));
                    vec.add(rs.getString("borrowDate").substring(0,10));
                    vec.add(rs.getString("expireDate").substring(0,10));
                    String returnDate = rs.getString("returnDate");
                    if(StringUtil.isNotEmpty(returnDate)){
                        returnDate = returnDate.substring(0,10);
                    }
                    vec.add(returnDate);
                    dtm.addRow(vec);
                }
                con.close();
            }
            else{
                rs = borrowInfo.listAllTeacherBorrowHistory(con);
                while(rs.next()){
                    Vector vec = new Vector();
                    vec.add(rs.getInt("ID"));
                    vec.add(rs.getString("teaID"));
                    vec.add(rs.getString("teaName"));
                    vec.add(rs.getString("equipID"));
                    vec.add(rs.getString("equipName"));
                    vec.add(rs.getString("borrowDate").substring(0,10));
                    vec.add(rs.getString("expireDate").substring(0,10));
                    String returnDate = rs.getString("returnDate");
                    if(StringUtil.isNotEmpty(returnDate)){
                        returnDate = returnDate.substring(0,10);
                    }
                    vec.add(returnDate);
                    dtm.addRow(vec);
                }
                con.close();
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }
    //更新借用历史信息表格
    private void updateBorrowHistoryTable(String type){
        DefaultTableModel dtm = (DefaultTableModel)borrowHistoryTable.getModel();
        dtm.setRowCount(0);
        Connection con = null;
        try{
            con = dbUtil.getConnection();
            ResultSet rs;
            if(type.equals("按学生")){
                rs = borrowInfo.listAllStudentBorrowHistoryByID(con,this.stuOrTeaIDText.getText());
                int rsIsEmptyFlag = 0;
                while(rs.next()){
                    rsIsEmptyFlag = 1;
                    Vector vec = new Vector();
                    vec.add(rs.getString("stuID"));
                    vec.add(rs.getString("stuName"));
                    vec.add(rs.getString("equipID"));
                    vec.add(rs.getString("equipName"));
                    vec.add(rs.getString("borrowDate").substring(0,10));
                    vec.add(rs.getString("expireDate").substring(0,10));
                    String returnDate = rs.getString("returnDate");
                    if(StringUtil.isNotEmpty(returnDate)){
                        returnDate = returnDate.substring(0,10);
                    }
                    vec.add(returnDate);
                    dtm.addRow(vec);
                }
                con.close();
                if(rsIsEmptyFlag == 0){
                    JOptionPane.showMessageDialog(null, "查无此信息！");
                }
            }
            else{
                rs = borrowInfo.listAllTeacherBorrowHistoryByID(con,this.stuOrTeaIDText.getText());
                int rsIsEmptyFlag = 0;
                while(rs.next()){
                    rsIsEmptyFlag = 1;
                    Vector vec = new Vector();
                    vec.add(rs.getString("teaID"));
                    vec.add(rs.getString("teaName"));
                    vec.add(rs.getString("equipID"));
                    vec.add(rs.getString("equipName"));
                    vec.add(rs.getString("borrowDate").substring(0,10));
                    vec.add(rs.getString("expireDate").substring(0,10));
                    String returnDate = rs.getString("returnDate");
                    if(StringUtil.isNotEmpty(returnDate)){
                        returnDate = returnDate.substring(0,10);
                    }
                    vec.add(returnDate);
                    dtm.addRow(vec);
                }
                con.close();
                if(rsIsEmptyFlag == 0){
                    JOptionPane.showMessageDialog(null, "查无此信息！");
                }
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }
