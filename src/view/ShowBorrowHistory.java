    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed

        // TODO add your handling code here:
        //根据学号或教工号查询
        String stuOrTeaIDText = this.stuOrTeaIDText.getText();
        if(StringUtil.isEmpty(stuOrTeaIDText)){
            JOptionPane.showMessageDialog(null,"输入不能为空！");
            return;
        }
        if(!StringUtil.isAllNumber(stuOrTeaIDText)){
            JOptionPane.showMessageDialog(null,"请输入正确格式的号码！");
            return;
        }
        if(type.equals("按学生") && stuOrTeaIDText.length() != 14){
            JOptionPane.showMessageDialog(null,"请输入14位的学号！");
            return;
        }
        if(type.equals("按老师") && stuOrTeaIDText.length() != 11){
            JOptionPane.showMessageDialog(null,"请输入11位的教工号！");
            return;
        }
        this.updateBorrowHistoryTable(type);
        resetValues();
    }//GEN-LAST:event_searchButtonActionPerformed

