    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed

        // TODO add your handling code here:
        //根据编号或名称查询
        Equipment equip = new Equipment();
        String idOrNameText = this.IDOrNameText.getText();
        if(StringUtil.isEmpty(idOrNameText)){
            JOptionPane.showMessageDialog(null,"输入不能为空！");
            return;
        }
        if(type.equals("按名称")){
            equip.setEquipName(idOrNameText);
        }
        if(type.equals("按编号") && !StringUtil.isAllNumber(idOrNameText)){
            JOptionPane.showMessageDialog(null,"请输入正确格式的编号！");
            return;
        }
        this.updateTable(equip);
        resetSearchValues();
    }//GEN-LAST:event_searchButtonActionPerformed

