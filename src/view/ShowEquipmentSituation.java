    //初始化表格
    private void initTable(Equipment equip){
        DefaultTableModel dtm = (DefaultTableModel)equipTable.getModel();
        dtm.setRowCount(0);
        Connection con = null;
        try{
            con = dbUtil.getConnection();
            ResultSet rs;
            rs = equipmentInfo.listByName(con,equip);
            while(rs.next()){
                Vector vec = new Vector();
                vec.add(rs.getInt("equipID"));
                vec.add(rs.getString("equipName"));
                vec.add(rs.getString("equipDescription"));
                vec.add(rs.getString("isBorrowed"));
                vec.add(rs.getString("isAvailable"));
                vec.add(rs.getFloat("unitPrice"));
                vec.add(rs.getString("registerDate").substring(0, 10));
                String scrapDate = rs.getString("scrapDate");
                if(StringUtil.isNotEmpty(scrapDate)){
                    scrapDate = scrapDate.substring(0,10);
                }
                vec.add(scrapDate);
                dtm.addRow(vec);
            }
            con.close();
        }catch(Exception e){
            e.printStackTrace();
        }
    }
    //更新表格
    private void updateTable(Equipment equip){
        DefaultTableModel dtm = (DefaultTableModel)equipTable.getModel();
        dtm.setRowCount(0);
        Connection con = null;
        try{
            con = dbUtil.getConnection();
            ResultSet rs;
            if(type.equals("按编号")){
                rs = equipmentInfo.listByID(con,this.IDOrNameText.getText());
            }
            else{
                rs = equipmentInfo.listByName(con,equip);
            }
            int rsIsEmptyFlag = 0;
            while(rs.next()){
                rsIsEmptyFlag =1;
                Vector vec = new Vector();
                vec.add(rs.getInt("equipID"));
                vec.add(rs.getString("equipName"));
                vec.add(rs.getString("equipDescription"));
                vec.add(rs.getString("isBorrowed"));
                vec.add(rs.getString("isAvailable"));
                vec.add(rs.getFloat("unitPrice"));
                vec.add(rs.getString("registerDate").substring(0, 10));
                String scrapDate = rs.getString("scrapDate");
                if(StringUtil.isNotEmpty(scrapDate)){
                    scrapDate = scrapDate.substring(0,10);
                }
                vec.add(scrapDate);
                dtm.addRow(vec);
            }
            con.close();
            if(rsIsEmptyFlag == 0){
                JOptionPane.showMessageDialog(null, "查无此器材！");
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }

