    //初始化表格
    private void initTable(Student stu){
        DefaultTableModel dtm = (DefaultTableModel)stuTable.getModel();
        dtm.setRowCount(0);
        Connection con = null;
        try{
            con = dbUtil.getConnection();
            ResultSet rs;
            rs = studentInfo.listByName(con,stu);
            while(rs.next()){
                Vector vec = new Vector();
                vec.add(rs.getString("stuID"));
                vec.add(rs.getString("stuName"));
                vec.add(rs.getString("stuSex"));
                vec.add(rs.getString("stuCollege"));
                vec.add(rs.getString("stuDepartment"));
                vec.add(rs.getString("stuType"));
                vec.add(rs.getString("stuPhone"));
                vec.add(rs.getString("stuRemark"));
                dtm.addRow(vec);
            }
            con.close();
        }catch(Exception e){
            e.printStackTrace();
        }
    }
    //更新表格
    private void updateTable(Student stu){
        DefaultTableModel dtm = (DefaultTableModel)stuTable.getModel();
        dtm.setRowCount(0);
        Connection con = null;
        try{
            con = dbUtil.getConnection();
            ResultSet rs;
            if(type.equals("按学号")){
                rs = studentInfo.listByID(con,this.IDOrNameText.getText());
            }
            else{
                rs = studentInfo.listByName(con,stu);
            }
            int rsIsEmptyFlag = 0;
            while(rs.next()){
                rsIsEmptyFlag = 1;
                Vector vec = new Vector();
                vec.add(rs.getString("stuID"));
                vec.add(rs.getString("stuName"));
                vec.add(rs.getString("stuSex"));
                vec.add(rs.getString("stuCollege"));
                vec.add(rs.getString("stuDepartment"));
                vec.add(rs.getString("stuType"));
                vec.add(rs.getString("stuPhone"));
                vec.add(rs.getString("stuRemark"));
                dtm.addRow(vec);
            }
            con.close();
            if(rsIsEmptyFlag == 0){
                JOptionPane.showMessageDialog(null, "查无此人！");
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }

