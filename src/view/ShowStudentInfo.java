    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed

        // TODO add your handling code here:
        //根据学号或姓名查询
        Student stu = new Student();
        String idOrNameText = this.IDOrNameText.getText();
        if(StringUtil.isEmpty(idOrNameText)){
            JOptionPane.showMessageDialog(null,"输入不能为空！");
            return;
        }
        if(type.equals("按姓名")){
            stu.setStuName(idOrNameText);
        }
        if(type.equals("按学号") && !StringUtil.isAllNumber(idOrNameText)){
            JOptionPane.showMessageDialog(null,"请输入正确格式的学号！");
            return;
        }
        if(type.equals("按学号") && idOrNameText.length() != 14){
            JOptionPane.showMessageDialog(null,"请输入14位的学号！");
            return;
        }
        this.updateTable(stu);
        resetSearchValues();
    }//GEN-LAST:event_searchButtonActionPerformed

