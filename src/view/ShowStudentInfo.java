    private void modifyActionPerformed(ActionEvent evt) throws Exception {
        //throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
        String stuID = this.stuIDText.getText();
        String stuCollege = this.stuCollegeText.getText();
        String stuDepartment = this.stuDepartmentText.getText();
        String stuPhone = this.stuPhoneText.getText();
        String stuRemark = this.stuRemarkText.getText();
        if(StringUtil.isEmpty(stuID)){
            JOptionPane.showMessageDialog(null, "请先选择一条学生信息！");
            return;
        }
        if(StringUtil.isEmpty(stuCollege)){
            JOptionPane.showMessageDialog(null, "学院不能为空！");
            return;
        }
        if(StringUtil.isEmpty(stuDepartment)){
            JOptionPane.showMessageDialog(null, "系不能为空！");
            return;
        }
        if(StringUtil.isEmpty(stuPhone)){
            JOptionPane.showMessageDialog(null, "手机不能为空！");
            return;
        }
        if(!StringUtil.isAllNumber(stuPhone)){
            JOptionPane.showMessageDialog(null, "请输入合法的手机号！");
            return;
        }
        if(stuPhone.length() != 11){
            JOptionPane.showMessageDialog(null, "请输入11位的手机号！");
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(null, "确认修改？");
        if(confirm == 0){//确认修改
            Student stu = new Student(stuID,stuCollege,stuDepartment,stuPhone,stuRemark);
            Connection con = dbUtil.getConnection();
            int modify = studentInfo.updateInfo(con, stu);
            if(modify == 1){
                JOptionPane.showMessageDialog(null, "修改成功！");
                resetValues();
            }
            else{
                JOptionPane.showMessageDialog(null, "修改失败！");
                return;
            }
            initTable(new Student());//初始化表格
        }
    }

