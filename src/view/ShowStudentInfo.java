    private void deleteActionPerformed(ActionEvent evt) throws Exception {
        //throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
        String stuID = this.stuIDText.getText();
        if(StringUtil.isEmpty(stuID)){
            JOptionPane.showMessageDialog(null, "请先选择一条学生信息！");
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(null, "确认删除？");
        if(confirm == 0){//确认删除
            Connection con = dbUtil.getConnection();
            int delete = studentInfo.deleteInfo(con, stuID);
            if(delete == 1){
                JOptionPane.showMessageDialog(null, "删除成功！");
                resetValues();
            }
            else{
                JOptionPane.showMessageDialog(null, "删除失败！");
                return;
            }
            initTable(new Student());//初始化表格
        }
    }

