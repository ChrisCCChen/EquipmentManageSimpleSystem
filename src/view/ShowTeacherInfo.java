    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed

        // TODO add your handling code here:
        //根据教工号或姓名查询
        Teacher tea = new Teacher();
        String idOrNameText = this.IDOrNameText.getText();
        if(StringUtil.isEmpty(idOrNameText)){
            JOptionPane.showMessageDialog(null,"输入不能为空！");
            return;
        }
        if(type.equals("按姓名")){
            tea.setTeaName(idOrNameText);
        }
        if(type.equals("按教工号") && !StringUtil.isAllNumber(idOrNameText)){
            JOptionPane.showMessageDialog(null,"请输入正确格式的教工号！");
            return;
        }
        if(type.equals("按教工号") && idOrNameText.length() != 11){
            JOptionPane.showMessageDialog(null,"请输入11位的教工号！");
            return;
        }
        this.updateTable(tea);
        resetSearchValues();
    }//GEN-LAST:event_searchButtonActionPerformed

