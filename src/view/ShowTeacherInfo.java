    private void modifyActionPerformed(ActionEvent evt) throws Exception {
        //throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
        String teaID = this.teaIDText.getText();
        String teaCollege = this.teaCollegeText.getText();
        String teaDepartment = this.teaDepartmentText.getText();
        String teaType = this.teaTypeText.getText();
        String teaPhone = this.teaPhoneText.getText();
        String teaRemark = this.teaRemarkText.getText();
        if(StringUtil.isEmpty(teaID)){
            JOptionPane.showMessageDialog(null, "请先选择一条老师信息！");
            return;
        }
        if(StringUtil.isEmpty(teaCollege)){
            JOptionPane.showMessageDialog(null, "学院不能为空！");
            return;
        }
        if(StringUtil.isEmpty(teaDepartment)){
            JOptionPane.showMessageDialog(null, "系不能为空！");
            return;
        }
        if(StringUtil.isEmpty(teaType)){
            JOptionPane.showMessageDialog(null, "类型不能为空！");
            return;
        }
        if(StringUtil.isEmpty(teaPhone)){
            JOptionPane.showMessageDialog(null, "手机不能为空！");
            return;
        }
        if(!StringUtil.isAllNumber(teaPhone)){
            JOptionPane.showMessageDialog(null, "请输入合法的手机号！");
            return;
        }
        if(teaPhone.length() != 11){
            JOptionPane.showMessageDialog(null, "请输入11位的手机号！");
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(null, "确认修改？");
        if(confirm == 0){//确认修改
            Teacher tea = new Teacher(teaID,teaCollege,teaDepartment,teaType,teaPhone,teaRemark);
            Connection con = dbUtil.getConnection();
            int modify = teacherInfo.updateInfo(con, tea);
            if(modify == 1){
                JOptionPane.showMessageDialog(null, "修改成功！");
                resetValues();
            }
            else{
                JOptionPane.showMessageDialog(null, "修改失败！");
                return;
            }
            initTable(new Teacher());//初始化表格
        }
    }

    private void deleteActionPerformed(ActionEvent evt) throws Exception {
        //throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
        String teaID = this.teaIDText.getText();
        if(StringUtil.isEmpty(teaID)){
            JOptionPane.showMessageDialog(null, "请先选择一条老师信息！");
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(null, "确认删除？");
        if(confirm == 0){//确认删除
            Connection con = dbUtil.getConnection();
            int delete = teacherInfo.deleteInfo(con, teaID);
            if(delete == 1){
                JOptionPane.showMessageDialog(null, "删除成功！");
                resetValues();
            }
            else{
                JOptionPane.showMessageDialog(null, "删除失败！");
                return;
            }
            initTable(new Teacher());//初始化表格
        }
    }

