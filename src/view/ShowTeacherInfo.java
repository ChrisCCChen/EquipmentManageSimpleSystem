    //初始化表格
    private void initTable(Teacher tea){
        DefaultTableModel dtm = (DefaultTableModel)teaTable.getModel();
        dtm.setRowCount(0);
        Connection con = null;
        try{
            con = dbUtil.getConnection();
            ResultSet rs;
            rs = teacherInfo.listByName(con,tea);
            while(rs.next()){
                Vector vec = new Vector();
                vec.add(rs.getString("teaID"));
                vec.add(rs.getString("teaName"));
                vec.add(rs.getString("teaSex"));
                vec.add(rs.getString("teaCollege"));
                vec.add(rs.getString("teaDepartment"));
                vec.add(rs.getString("teaType"));
                vec.add(rs.getString("teaPhone"));
                vec.add(rs.getString("teaRemark"));
                dtm.addRow(vec);
            }
            con.close();
        }catch(Exception e){
            e.printStackTrace();
        }
    }
    //更新表格
    private void updateTable(Teacher tea){
        DefaultTableModel dtm = (DefaultTableModel)teaTable.getModel();
        dtm.setRowCount(0);
        Connection con = null;
        try{
            con = dbUtil.getConnection();
            ResultSet rs;
            if(type.equals("按教工号")){
                rs = teacherInfo.listByID(con,this.IDOrNameText.getText());
            }
            else{
                rs = teacherInfo.listByName(con,tea);
            }
            int rsIsEmptyFlag = 0;
            while(rs.next()){
                rsIsEmptyFlag = 1;
                Vector vec = new Vector();
                vec.add(rs.getString("teaID"));
                vec.add(rs.getString("teaName"));
                vec.add(rs.getString("teaSex"));
                vec.add(rs.getString("teaCollege"));
                vec.add(rs.getString("teaDepartment"));
                vec.add(rs.getString("teaType"));
                vec.add(rs.getString("teaPhone"));
                vec.add(rs.getString("teaRemark"));
                dtm.addRow(vec);
            }
            con.close();
            if(rsIsEmptyFlag == 0){
                JOptionPane.showMessageDialog(null, "查无此人！");
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }

